
public class Main02 {

	public static void main(String[] args) {
		long l = 100L;
		int i = (int) l;
		short s = (short) i;
		
		System.out.println("l = " + l);
		System.out.println("i = " + i);
		System.out.println("s = " + s);
		
		double pi1 = 3.14D;
		
		int pi2 = (int) pi1;
		
		System.out.println("pi1 = " + pi1);
		System.out.println("pi2 = " + pi2);
		
		int num1 = 97;
		
		char c2 = (char) (num1 + 2);
		System.out.println("c2 = " + c2);
		

	}

}
